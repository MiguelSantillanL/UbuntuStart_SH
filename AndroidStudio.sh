#Descargar y instalar Android Studio
wget https://redirector.gvt1.com/edgedl/android/studio/ide-zips/2021.1.1.22/android-studio-2021.1.1.22-linux.tar.gz

tar -xzvf android-studio*

sudo mv android-studio /opt/

cd /opt/android-studio/bin

./studio.sh

#Configurar el sistema para que Android Studio se ejecute al iniciar el sistema